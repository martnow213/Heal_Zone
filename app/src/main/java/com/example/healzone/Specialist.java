package com.example.healzone;

public class Specialist {

    String specialistName;
    String specialistSurname;
    String specialistNumber;
    String specialistEmail;
    String specialistCity;
    String specialistStreet;
    String specialistBuilding;

    public Specialist() {
    }

    public String getSpecialistName() {
        return specialistName;
    }

    public void setSpecialistName(String specialistName) {
        this.specialistName = specialistName;
    }

    public String getSpecialistSurname() {
        return specialistSurname;
    }

    public void setSpecialistSurname(String specialistSurname) {
        this.specialistSurname = specialistSurname;
    }

    public String getSpecialistNumber() {
        return specialistNumber;
    }

    public void setSpecialistNumber(String specialistNumber) {
        this.specialistNumber = specialistNumber;
    }

    public String getSpecialistEmail() {
        return specialistEmail;
    }

    public void setSpecialistEmail(String specialistEmail) {
        this.specialistEmail = specialistEmail;
    }

    public String getSpecialistCity() {
        return specialistCity;
    }

    public void setSpecialistCity(String specialistCity) {
        this.specialistCity = specialistCity;
    }

    public String getSpecialistStreet() {
        return specialistStreet;
    }

    public void setSpecialistStreet(String specialistStreet) {
        this.specialistStreet = specialistStreet;
    }

    public String getSpecialistBuilding() {
        return specialistBuilding;
    }

    public void setSpecialistBuilding(String specialistBuilding) {
        this.specialistBuilding = specialistBuilding;
    }
}
